function createQuizBlocks(chapters, questionsPerChapter, totalQuestions) {
  const quizBlocks = [];
  const questionsPerBlock = Math.floor(totalQuestions / chapters.length);

  chapters.forEach((chapter, index) => {
    const chapterQuestions = [];

    // Collect questions for the current chapter
    while (chapterQuestions.length < questionsPerChapter) {
      const randomIndex = Math.floor(Math.random() * chapter.length);
      chapterQuestions.push(chapter[randomIndex]);
      chapter.splice(randomIndex, 1); // Remove the selected question to avoid repetition
    }

    // Add questions from the current chapter to the quiz blocks
    for (let i = 0; i < questionsPerBlock; i++) {
      const randomQuestion = chapterQuestions[Math.floor(Math.random() * chapterQuestions.length)];
      quizBlocks.push(randomQuestion);
      chapterQuestions.splice(chapterQuestions.indexOf(randomQuestion), 1); // Remove the selected question to avoid repetition
    }
  });

  return quizBlocks;
}

